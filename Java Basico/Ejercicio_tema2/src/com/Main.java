package com;

public class Main {
    public static void main(String[] args) {
        //numeros
        //Enteros

        byte numero1 = 1;
        System.out.println("Numero tipo byte" + numero1);

        short numero2 = 2;
        System.out.println("numero tipo short" + numero2);

        int numero3 = 3;
        System.out.println("Numero tipo int" + numero3);

        long numero4 = 4;
        System.out.println("Numero tipo long" + numero4);

        //numero con punto flotante

        float numero5 = 5.0f;
        System.out.println("Numero con punto flotante" + numero5);

        double numero6 = 6.0;
        System.out.println("Numero con punto flotante" + numero6);

        // Tipos booleans

        boolean b1 = true;
        System.out.println("Tipo con booleans" + b1);
        boolean b2 = false;
        System.out.println("tipo con booleans" + b2);

        //Tipo caracteres

        char ca1 = 'a';
        System.out.println("tipo caracteres" + ca1);

        //Tipo cadena de caracteres

        String ca2 = "Hola Mundo";
        System.out.println("tipo cadena de caracteres" + ca2);




    }
}

